// --- granularity helpers ---

/**
 * Valid granularity suffixes that can be appended to dataset field IDs
 */
const VALID_GRANULARITY_SUFFIXES = [
    "minute",
    "hour",
    "day",
    "week",
    "month",
    "quarter",
    "year",
    "minuteOfHour",
    "hourOfDay",
    "dayOfWeek",
    "dayOfMonth",
    "dayOfYear",
    "weekOfYear",
    "monthOfYear",
    "quarterOfYear",
] as const;

/**
 * Regular expression to detect granularity suffixes at the end of an ID
 */
const GRANULARITY_SUFFIX_REGEX = new RegExp(`\\.(${VALID_GRANULARITY_SUFFIXES.join("|")})$`);

/**
 * Check if an ID has a valid granularity suffix
 * @param id - The ID to check
 * @returns true if the ID ends with a valid granularity suffix
 */
function hasGranularitySuffix(id: string): boolean {
    return GRANULARITY_SUFFIX_REGEX.test(id);
}

/**
 * Determine the correct type for an ID based on whether it has a granularity suffix
 * and whether the base ID is in the dates list
 * @param id - The ID to check
 * @param datesId - Array of date dataset IDs
 * @returns "attribute" if has suffix, "date" if base ID is in datesId, otherwise "attribute"
 */
export function determineTypeForId(id: string, datesId: string[]): "attribute" | "date" {
    if (hasGranularitySuffix(id)) {
        return "attribute";
    }
    return datesId.includes(id) ? "date" : "attribute";
}
