export function convertGranularity(
    gran: Required<DateDataset>["granularities"][number] | any,
): DateAttributeGranularity | null {
    const g = gran as Required<DateDataset>["granularities"][number];
    switch (g) {
        case "MINUTE":
            return "GDC.time.minute";
        case "HOUR":
            return "GDC.time.hour";
        case "DAY":
            return "GDC.time.date";
        case "WEEK":
            return "GDC.time.week";
        case "WEEK_US":
            return "GDC.time.week_us";
        case "MONTH":
            return "GDC.time.month";
        case "QUARTER":
            return "GDC.time.quarter";
        case "YEAR":
            return "GDC.time.year";
        case "MINUTE_OF_HOUR":
            return "GDC.time.minute_in_hour";
        case "HOUR_OF_DAY":
            return "GDC.time.hour_in_day";
        case "DAY_OF_WEEK":
            return "GDC.time.day_in_week";
        case "DAY_OF_MONTH":
            return "GDC.time.day_in_month";
        case "DAY_OF_YEAR":
            return "GDC.time.day_in_year";
        case "WEEK_OF_YEAR":
            return "GDC.time.week_in_year";
        case "MONTH_OF_YEAR":
            return "GDC.time.month_in_year";
        case "QUARTER_OF_YEAR":
            return "GDC.time.quarter_in_year";
        case "DAY_OF_WEEK_EU":
            return "GDC.time.day_in_euweek";
        case "WEEK_OF_YEAR_EU":
            return "GDC.time.euweek_in_year";
        case "WEEK_OF_QUARTER_EU":
            return "GDC.time.euweek_in_quarter";
        case "WEEK_OF_QUARTER":
            return "GDC.time.week_in_quarter";
        case "MONTH_OF_QUARTER":
            return "GDC.time.month_in_quarter";
        case "DAY_OF_QUARTER":
            return "GDC.time.day_in_quarter";
        case "FISCAL_YEAR":
            return "GDC.time.fiscal_year";
        case "FISCAL_QUARTER":
            return "GDC.time.fiscal_quarter";
        case "FISCAL_MONTH":
            return "GDC.time.fiscal_month";
        default:
            assertUnreachable(g);
    }
}

export function parseGranularity(
    gran: DateAttributeGranularity | null,
): Required<DateDataset>["granularities"][number] | null {
    switch (gran) {
        case "GDC.time.minute":
            return "MINUTE";
        case "GDC.time.hour":
            return "HOUR";
        case "GDC.time.date":
            return "DAY";
        case "GDC.time.week":
            return "WEEK";
        case "GDC.time.week_us":
            return "WEEK_US";
        case "GDC.time.month":
            return "MONTH";
        case "GDC.time.quarter":
            return "QUARTER";
        case "GDC.time.year":
            return "YEAR";
        case "GDC.time.minute_in_hour":
            return "MINUTE_OF_HOUR";
        case "GDC.time.hour_in_day":
            return "HOUR_OF_DAY";
        case "GDC.time.day_in_week":
            return "DAY_OF_WEEK";
        case "GDC.time.day_in_euweek":
            return "DAY_OF_WEEK_EU";
        case "GDC.time.day_in_month":
            return "DAY_OF_MONTH";
        case "GDC.time.day_in_year":
            return "DAY_OF_YEAR";
        case "GDC.time.week_in_year":
            return "WEEK_OF_YEAR";
        case "GDC.time.euweek_in_year":
            return "WEEK_OF_YEAR_EU";
        case "GDC.time.month_in_year":
            return "MONTH_OF_YEAR";
        case "GDC.time.quarter_in_year":
            return "QUARTER_OF_YEAR";
        case "GDC.time.euweek_in_quarter":
            return "WEEK_OF_QUARTER_EU";
        case "GDC.time.week_in_quarter":
            return "WEEK_OF_QUARTER";
        case "GDC.time.month_in_quarter":
            return "MONTH_OF_QUARTER";
        case "GDC.time.day_in_quarter":
            return "DAY_OF_QUARTER";
        case "GDC.time.fiscal_year":
            return "FISCAL_YEAR";
        case "GDC.time.fiscal_quarter":
            return "FISCAL_QUARTER";
        case "GDC.time.fiscal_month":
            return "FISCAL_MONTH";
        case null:
            return null;
        default:
            assertUnreachable(gran as never);
    }
}

