export function convertGranularityToDeclarativeDatasetGranularity(
    gran: Required<DateDataset>["granularities"][number],
): DeclarativeDateDataset["granularities"][number] {
    switch (gran) {
        case "DAY":
            return "DAY";
        case "DAY_OF_MONTH":
            return "DAY_OF_MONTH";
        case "DAY_OF_WEEK":
        case "DAY_OF_WEEK_EU":
            return "DAY_OF_WEEK";
        case "DAY_OF_YEAR":
            return "DAY_OF_YEAR";
        case "HOUR":
            return "HOUR";
        case "HOUR_OF_DAY":
            return "HOUR_OF_DAY";
        case "MINUTE":
            return "MINUTE";
        case "MINUTE_OF_HOUR":
            return "MINUTE_OF_HOUR";
        case "MONTH":
            return "MONTH";
        case "MONTH_OF_YEAR":
            return "MONTH_OF_YEAR";
        case "QUARTER":
            return "QUARTER";
        case "QUARTER_OF_YEAR":
            return "QUARTER_OF_YEAR";
        case "WEEK":
        case "WEEK_US":
            return "WEEK";
        case "WEEK_OF_YEAR":
        case "WEEK_OF_YEAR_EU":
            return "WEEK_OF_YEAR";
        case "YEAR":
            return "YEAR";
        case "WEEK_OF_QUARTER":
        case "WEEK_OF_QUARTER_EU":
        case "MONTH_OF_QUARTER":
        case "DAY_OF_QUARTER":
            //NOTE: This value is not supported in the declarative model dataset, there are only available for DateAttributeGranularity but
            // we want to have a consistent in all places. For DeclarativeDateDataset, we will return "QUARTER" as it necessary for the support.
            // That mean user can use it in yaml but will not be propagated to the declarative model and removed when cloning.
            return "QUARTER";
        case "FISCAL_YEAR":
            return "FISCAL_YEAR";
        case "FISCAL_QUARTER":
            return "FISCAL_QUARTER";
        case "FISCAL_MONTH":
            return "FISCAL_MONTH";
        default:
            assertUnreachable(gran);
    }
}

